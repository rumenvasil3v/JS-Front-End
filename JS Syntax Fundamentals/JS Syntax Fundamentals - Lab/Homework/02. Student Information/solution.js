function solve(studentName, studentAge, studentAverageGrade) {
    console.log(`Name: ${studentName}, Age: ${studentAge}, Grade: ${studentAverageGrade.toFixed(2)}`);
}

solve('John', 15, 5.54678); // Name: John, Age: 15, Grade: 5.55
solve('Steve', 16, 2.1426); // Name: Steve, Age: 16, Grade: 2.14
solve('Marry', 12, 6.00); // Name: Marry, Age: 12, Grade: 6.00